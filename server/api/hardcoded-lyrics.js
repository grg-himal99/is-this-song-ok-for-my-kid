export default defineEventHandler(async (event) => {
  const query = getQuery(event);
  const { artist, title } = query;

  if (!artist || !title) {
    throw createError({
      statusCode: 400,
      statusMessage: 'Artist and title are required',
    });
  }

  // Normalize the search terms
  const normalizedArtist = artist.toLowerCase();
  const normalizedTitle = title.toLowerCase();

  // Check if we have a match in our hardcoded lyrics
  for (const [key, song] of Object.entries(hardcodedLyrics)) {
    // Check if either the key, title or artist contains the search terms
    if (
      key.includes(normalizedTitle) || 
      normalizedTitle.includes(key) ||
      song.title.toLowerCase().includes(normalizedTitle) ||
      normalizedTitle.includes(song.title.toLowerCase()) ||
      song.artist.toLowerCase().includes(normalizedArtist) ||
      normalizedArtist.includes(song.artist.toLowerCase())
    ) {
      return {
        lyrics: song.lyrics,
        language: song.language
      };
    }
  }

  // No match found
  throw createError({
    statusCode: 404,
    statusMessage: 'Lyrics not found in hardcoded collection',
  });
});